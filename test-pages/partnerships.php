                            <h3>Khyentse Foundation</h3>
                        </div>
                        <div class="col-sm-9">
                            <p>Khyentse Foundation is a nonprofit organization founded in 2001 by Dzongsar Khyentse Rinpoche to support all traditions of Buddhist study and practice. Its activities include major text preservation and translation projects, support for traditional monastic colleges in Asia, a worldwide scholarship and awards program, academic development of Buddhist studies in major universities, training and development for Buddhist leaders and teachers, Buddhist education for children, support for individual study and retreats, and more.</p>
                            <p>84000 began as an initiative of the Khyentse Foundation, and was incubated by the Foundation for four years before launching as its own <a href="http://84000.co/84000-matures-to-independence/">independent non-profit</a> in 2013. The two organizations continue to enjoy a close working relationship on many levels.</p>
                            <p>For more information on Khyentse Foundation, please visit: <a href="http://www.khyentsefoundation.org">www.khyentsefoundation.org</a></p>
                            <h4>The Kumarajiva Plan</h4>
                            <p>In 2019, Khyentse Foundation launched <em>Kumarajiva Plan</em>, a new initiative that aims to translate into Chinese all the texts in the Tibetan canon which are not available in the Chinese canon. 84000 has been instrumental in sharing planning documents, lessons learned, procedural protocol, and other experience and information with the new project.</p>
                            <p>For more information on Kumarajiva Plan, please visit: <a href="#">www.?</a>.</p>
