                            <section id="body-title" class="page indent">
                                <a href="#body-title" class="milestone" title="Bookmark this section">Tr.</a>
                                <h3>The Translation</h3>
                                <h2>The Noble Mahāyāna Sūtra</h2>
                                <h1>The Question of Maitreya</h1>
                            </section>

                            <section class="chapter page indent" id="chapter-1">
                                <p id="6f1d167c-a328-434e-a38d-5ab055f52503">
                                    <a class="milestone" title="Bookmark this section" href="#6f1d167c-a328-434e-a38d-5ab055f52503">C1.1</a>
                                    [F.104.b] Homage to all the buddhas and bodhisattvas!
                                </p>
                                <p id="64fcf49d-9c4f-44cc-94ba-933c529ed4a1">
                                    <a class="milestone" title="Bookmark this section" href="#64fcf49d-9c4f-44cc-94ba-933c529ed4a1">C1.2</a>
                                    Thus have I heard at one time. The Blessed One was staying in the Country of the Bhargas, on Mount Śuśumāra in a fearsome forest of wild animals,     together with a great saṅgha of about 500 monks, eminent śrāvaka-elders who possessed clairvoyance. These included the omniscient venerable Kauṇḍinya, and the venerables Aśvajit, Vaṣpa, Mahānāman, Bhadrika, Vimala, Pūrṇa, Gavāṃpati, Mṛgaśiras, Uruvilva Kāśyapa, Nadī Kāśyapa, Gayā Kāśyapa, Jaṅghā Kāśyapa, Mahākāśyapa, Amogharāja, Sumati, Śāriputra, the eminent Maudgalyāyana, Parvatarāja, Nanda, Upananda, Ānanda, Rāhula, and so on. [F.105.a]
                                </p>
                                <p id="31232edd-d2af-4518-9941-c4a0cc13f57a">
                                    <a class="milestone" title="Bookmark this section" href="#31232edd-d2af-4518-9941-c4a0cc13f57a">C1.3</a>
                                    Maitreya and 10,000 other bodhisattva mahāsattvas endowed with clairvoyance had also assembled there. These included the bodhisattvas Vardhamānamati, Sumati, Sthiramati, Viśālamati, Pratibhānakūṭa, Avalokiteśvara Mahāsthāmaprāpta, Kusumaśrī, Mañjuśrī, Abhijñāpuṣpasuparipūrṇa, Anupalambhamanasikāra, Prasādavat, Ratnaketu, Indradeva, Varuṇadeva,<sup><a class="footnote pop-up" href="#footnote-1" id="footnote-link1">1</a></sup> Mṛdusvara, Kṣetraviśodhana, Merukūṭa, Pratibhānasaṃpad, Indraśrī, Nimiṃdhara, Scourer of the Hell-Dwellers, Abandoner of Unfavorable Rebirths, Ratnakesarin and others.
                                </p>
                                <p id="5862ea40-0623-44a6-a875-896f37ac562f">
                                    <a class="milestone" title="Bookmark this section" href="#5862ea40-0623-44a6-a875-896f37ac562f">C1.4</a>
                                    “For a long time, like a yak its tail,<sup><a class="footnote pop-up" href="#footnote-2" id="footnote-link2">2</a></sup><br>
                                    You observed unimpaired moral conduct and stuff.<br>
                                    Peerless in your conduct,<br>
                                    You wholly perfected the conduct of a buddha.<br>
                                </p>
                                <p id="f958dcc3-aa1a-4dc5-ba53-23f206104636">
                                    The four great oceans,<br>
                                    and the sphere of nothingness.<br>
                                </p>
                                <p id="6def71d1-f5b2-4c1c-afcf-2107fa74f7d9">
                                    And a test of the plural of sphere of nothingnesses and domness.
                                </p>
                            </section>

