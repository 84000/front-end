<section id="summary" class="relative page"><a href="#summary" class="bookmark" title="Bookmark this section">S.</a><h3>Summary</h3><p id="62fbb590-fae5-4116-8c1b-866cdc99d5ae" class="glossarized"><a class="bookmark" title="Bookmark this section" href="#62fbb590-fae5-4116-8c1b-866cdc99d5ae">S.1</a>In <em lang="en">The Question of Maitreya</em>, the Bodhisattva
Maitreya asks the Buddha what qualities a bodhisattva needs to attain
enlightenment quickly. The Buddha outlines several sets of qualities, foremost
among them the altruistic intention of perfect bodhicitta. The Buddha then
recounts to Ānanda how, in a former life, Maitreya revered a previous Buddha
and, wishing to become just like him, at once realized that all phenomena are
unproduced. Ānanda asks why Maitreya did not become a buddha sooner, and in
answer the Buddha compares Maitreya’s bodhisattva career with his own, listing
further sets of qualities that differentiate them and recounting examples of the
hardships he himself faced in previous lives. Maitreya, on the other hand, has
followed the easy bodhisattva vehicle using its skillful means, such as the
seven branch practice and the training in the six perfections; the aspirations
he thus made are set out in the famous “Prayer of Maitreya” for which this sūtra
is perhaps best known. The Buddha declares that Maitreya will become enlightened
when sentient beings have fewer negative emotions, in contrast to the ignorant
and turbulent beings he himself vowed to help. </p></section><section id="acknowledgment" class="relative page"><a href="#acknowledgment" class="bookmark" title="Bookmark this section">A.</a><h3>Acknowledgments</h3><p id="101dba40-98f7-4f2f-9394-50db03e8ba4f"><a class="bookmark" title="Bookmark this section" href="#101dba40-98f7-4f2f-9394-50db03e8ba4f">A.1</a>This sūtra was translated by Dr. Karen Liljenberg, with
Dr. Ulrich Pagel acting as consultant. </p><p id="67d04ba2-652f-441e-a7ac-e486a6176060">This translation has been completed under the patronage and supervision of 84000:
Translating the Words of the Buddha.</p></section><section id="introduction" class="relative page"><a href="#introduction" class="bookmark" title="Bookmark this section">I.</a><h3>Introduction</h3><p id="64add122-62c3-4793-adf7-c5aafdae7b15" class="glossarized"><a class="bookmark" title="Bookmark this section" href="#64add122-62c3-4793-adf7-c5aafdae7b15">I.1</a>
    The Question of Maitreya (Maitreyaparipṛcchā), In D this title, included within the text, is preceded by the
        introductory sentence: “From The Noble Great Heap of Jewels Dharma
        discourse in one hundred thousand sections, section 41: The Noble
        Elucidation of the Question of Maitreya in one fascicle” (’phags pa dkon mchog brtsegs pa chen po’i chos kyi
            rnam grangs le’u stong phrag brgya pa las le’u bzhi bcu rtsa gcig pa
            ste / ’phags pa byams pas zhus
            pa lung bstan pa / bam po gcig pa). like most of the
    sūtras that comprise the Ratnakūṭa collection, was translated into Tibetan from a Sanskrit original that is now
    lost, but that may have dated back as far as the 1st or 2nd centuries CE,
    i.e. several centuries before the collection itself was compiled. Apart from
    the Tibetan Kangyur version, translated in the 9th century, and a fragment
    in Sanskrit found in Central Asia,A fragment of the Stein
        collection, published in 1911 by Louis de La Vallée Poussin, was
        identified as part of this sūtra (corresponding to D, F.108.b – 110.a) 
        by Hisashi Matsumura in 1993 (see
        bibliography). the sūtra is extant only in an earlier Chinese
    translation.</p><p id="2b211a87-752e-45a7-8a58-ea8b0b9ec0ad" class="glossarized"><a class="bookmark" title="Bookmark this section" href="#2b211a87-752e-45a7-8a58-ea8b0b9ec0ad">I.2</a>The <a href="#53072a58-6037-4199-afe4-7b517e4fdce9" class="glossary" id="glossary-link-1-0">sphere of nothingness</a>.</p></section><section id="body-title" class="relative page"><a href="#body-title" class="bookmark" title="Bookmark this section">T.</a><h3>The Translation</h3><h2>The Noble Mahāyāna Sūtra</h2><h1>The Question of Maitreya</h1></section><section class="chapter relative page" id="chapter-1"><p id="6f1d167c-a328-434e-a38d-5ab055f52503" class="glossarized"><a class="bookmark" title="Bookmark this section" href="#6f1d167c-a328-434e-a38d-5ab055f52503">T1.1</a>
    
[F.104.b]
Homage to all the buddhas and bodhisattvas!</p><p id="64fcf49d-9c4f-44cc-94ba-933c529ed4a1" class="glossarized"><a class="bookmark" title="Bookmark this section" href="#64fcf49d-9c4f-44cc-94ba-933c529ed4a1">T1.2</a>Thus have I heard at one time. The Blessed One was staying in the Country of
    the Bhargas, on Mount Śuśumāra in a fearsome forest of wild animals,
    together with a great saṅgha of about 500 monks, eminent śrāvaka-elders who
    possessed clairvoyance. These included the omniscient venerable Kauṇḍinya,
    and the venerables Aśvajit, Vaṣpa, Mahānāman, Bhadrika, Vimala, Pūrṇa,
    Gavāṃpati, Mṛgaśiras, Uruvilva Kāśyapa, Nadī Kāśyapa, Gayā Kāśyapa, Jaṅghā
    Kāśyapa, Mahākāśyapa, Amogharāja, Sumati, Śāriputra, the eminent
    Maudgalyāyana, Parvatarāja, Nanda, Upananda, Ānanda, Rāhula, and so on. 
[F.105.a]

</p><p id="31232edd-d2af-4518-9941-c4a0cc13f57a" class="glossarized"><a class="bookmark" title="Bookmark this section" href="#31232edd-d2af-4518-9941-c4a0cc13f57a">T1.3</a>Maitreya and 10,000 other bodhisattva mahāsattvas endowed with clairvoyance
    had also assembled there. These included the bodhisattvas Vardhamānamati,
    Sumati, Sthiramati, Viśālamati, Pratibhānakūṭa, Avalokiteśvara,
    Mahāsthāmaprāpta, Kusumaśrī, Mañjuśrī, Abhijñāpuṣpasuparipūrṇa,
    Anupalambhamanasikāra, Prasādavat, Ratnaketu, Indradeva, Varuṇadeva,<sup><a class="footnote" href="#footnote-1" id="footnote-link1">1</a></sup> Mṛdusvara,
    Kṣetraviśodhana, Merukūṭa, Pratibhānasaṃpad, Indraśrī, Nimiṃdhara, Scourer
    of the Hell-Dwellers, Abandoner of Unfavorable Rebirths, Ratnakesarin and
    others. </p><p id="5862ea40-0623-44a6-a875-896f37ac562f" class="glossarized"><a class="bookmark" title="Bookmark this section" href="#5862ea40-0623-44a6-a875-896f37ac562f">T1.4</a>
    “For a long time, like a yak its tail,<sup><a class="footnote" href="#footnote-2" id="footnote-link2">2</a></sup>
    <br>
    You observed unimpaired moral conduct and stuff.<br>
    Peerless in your conduct,<br>
    You wholly perfected the conduct of a buddha.<br>
</p><p id="f958dcc3-aa1a-4dc5-ba53-23f206104636" class="glossarized">
    The <a href="#454ea8ae-d7da-4530-844d-b666ad59ae71" class="glossary" id="glossary-link-0-0">four great oceans</a>,<br>
    and the <a href="#53072a58-6037-4199-afe4-7b517e4fdce9" class="glossary" id="glossary-link-1-1">sphere of nothingness</a>.<br>
</p><p id="6def71d1-f5b2-4c1c-afcf-2107fa74f7d9" class="glossarized">
    And a test of the plural of <a href="#53072a58-6037-4199-afe4-7b517e4fdce9" class="glossary" id="glossary-link-1-2">sphere of nothingnesses</a> and domness.
</p></section><section id="colophon" class="relative page"><a href="#colophon" class="bookmark" title="Bookmark this section">C.</a><h3>Colophon</h3><div class="relative" id="chapter-1"><p id="dd9d7bd3-afa4-4ec8-844d-fbe020dae1a4" class="glossarized"><a class="bookmark" title="Bookmark this section" href="#dd9d7bd3-afa4-4ec8-844d-fbe020dae1a4">C1.1</a> This completes the Noble Great Vehicle Sūtra, <em lang="en">The
        Play in Full</em>.</p></div><div class="relative" id="chapter-2"><h4>Colophon to the Sanskrit Edition</h4><p id="47eae626-8b8b-479a-8506-2bbfb61d5943" class="glossarized"><a class="bookmark" title="Bookmark this section" href="#47eae626-8b8b-479a-8506-2bbfb61d5943">C2.1</a>
            The Thus-gone One explained the causes<br>
            Of those dharmas that have a cause<br>
            And also their cessation.<br>
            This is the teaching of the Great Ascetic.<br>
            May there be good goodness! May there be goodness in every
                way!<br>
        </p></div><div class="relative" id="chapter-3"><h4>Colophon to the Tibetan Translation</h4><p id="c40f291f-881c-49b5-a1b4-67c45f348855" class="glossarized"><a class="bookmark" title="Bookmark this section" href="#c40f291f-881c-49b5-a1b4-67c45f348855">C3.1</a>This was taught and translated by the Indian scholars Jinamitra,
            Dānaśila, and Munivarma, and the translator-editor Yeshe De, who
            proofed and finalized the translation. </p></div></section><section id="notes" class="relative page"><a href="#notes" class="bookmark" title="Bookmark this section">No.</a><h3>Notes</h3><p class="footnote" id="footnote-1"><a class="scroll-to-anchor footnote-number" href="#footnote-link1">1</a>This inclusion of Indradeva and Varuṇadeva, Vedic deities,
        among the assembly of attendant bodhisattvas is unusual. It is, however,
        paralleled in another Ratnakuṭa sūtra, the <em lang="sa-ltn">Tathāgatacintyaguhyanirdeśa</em> (Toh 47).</p><p class="footnote" id="footnote-2"><a class="scroll-to-anchor footnote-number" href="#footnote-link2">2</a>This odd,
                seemingly culturally Himalayan simile does not appear to be due
                to any domness in the text: N and H merely offer
                    <span lang="bo-ltn">yul ring(s)</span>
                instead of D <span lang="bo-ltn">yun
                    ring</span>. Indeed, the same simile is found in other
                Kangyur texts, again in the context of guarding moral
                conduct.</p></section><section id="glossary" class="relative page"><a href="#glossary" class="bookmark" title="Bookmark this section">Gl.</a><h3>Glossary</h3><div class="glossary-item glossarized backlinked" id="454ea8ae-d7da-4530-844d-b666ad59ae71"><div class="row"><div class="col-sm-8"><h4 class="term">Four great oceans</h4><p class="text-bo">རྒྱ་མཚོ་ཆེན་པོ་བཞི་</p><p>rgya mtsho chen po bzhi </p><p>catvaro samudrāḥ</p><p>Four oceans believed to surround the central
