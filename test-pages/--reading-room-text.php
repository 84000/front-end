                                <a href="#notes" class="milestone" title="Bookmark this section">N.</a>

                                <h3>Notes</h3>

                                <p class="footnote" id="footnote-1">
                                    <a class="scroll-to-anchor footnote-number" href="#footnote-link1">1.</a>
                                    This inclusion of Indradeva and Varuṇadeva, Vedic deities, among the assembly of attendant bodhisattvas is unusual. It is, however, paralleled in another Ratnakuṭa sūtra, the <em lang="sa-ltn">Tathāga­tacintyagu­hyanirdeśa</em> (Toh 47).
                                </p>
                                <p class="footnote" id="footnote-2">
                                    <a class="scroll-to-anchor footnote-number" href="#footnote-link2">2.</a>
                                    This odd, seemingly culturally Himalayan simile does not appear to be due to any domness in the text: N and H merely offer <span lang="bo-ltn">yul ring(s)</span> instead of D <span lang="bo-ltn">yun ring</span>. Indeed, the same simile is found in other Kangyur texts, again in the context of guarding moral conduct.
                                </p>
