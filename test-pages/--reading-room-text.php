                                <h3>Introduction</h3>
                                <p id="64add122-62c3-4793-adf7-c5aafdae7b15">
                                    <a class="milestone" title="Bookmark this section" href="#64add122-62c3-4793-adf7-c5aafdae7b15">I.1</a>
                                    The Question of Maitreya (Maitreyaparipṛcchā), In D this title, included within the text, is preceded by the introductory sentence: “From The Noble Great Heap of Jewels Dharma discourse in one hundred thousand sections, section 41: The Noble Elucidation of the Question of Maitreya in one fascicle” (’phags pa dkon mchog brtsegs pa chen po’i chos kyi rnam grangs le’u stong phrag brgya pa las le’u bzhi bcu rtsa gcig pa ste / ’phags pa byams pas zhus pa lung bstan pa / bam po gcig pa). like most of the sūtras that comprise the Ratnakūṭa collection, was translated into Tibetan from a Sanskrit original that is now lost, but that may have dated back as far as the 1st or 2nd centuries CE, i.e. several centuries before the collection itself was compiled. Apart from the Tibetan Kangyur version, translated in the 9th century, and a fragment in Sanskrit found in Central Asia,A fragment of the Stein collection, published in 1911 by Louis de La Vallée Poussin, was identified as part of this sūtra (corresponding to D, F.108.b – 110.a) by Hisashi Matsumura in 1993 (see bibliography). the sūtra is extant only in an earlier Chinese translation.
                                </p>
                                <p id="2b211a87-752e-45a7-8a58-ea8b0b9ec0ad">
                                    <a class="milestone" title="Bookmark this section" href="#2b211a87-752e-45a7-8a58-ea8b0b9ec0ad">I.2</a>
                                    The sphere of nothingness.
                                </p>
                                
                            </section>

