                                <a href="#summary" class="milestone" title="Bookmark this section">S.</a>
                                <h3>Summary</h3>
                                <p id="62fbb590-fae5-4116-8c1b-866cdc99d5ae">
                                    <a class="milestone" title="Bookmark this section" href="#62fbb590-fae5-4116-8c1b-866cdc99d5ae">S.1</a>
                                    In <em lang="en">The Question of Maitreya</em>, the Bodhisattva Maitreya asks the Buddha what qualities a bodhisattva needs to attain enlightenment quickly. The Buddha outlines several sets of qualities, foremost among them the altruistic intention of perfect bodhicitta. The Buddha then recounts to Ānanda how, in a former life, Maitreya revered a previous Buddha and, wishing to become just like him, at once realized that all phenomena are unproduced. Ānanda asks why Maitreya did not become a buddha sooner, and in answer the Buddha compares Maitreya’s bodhisattva career with his own, listing further sets of qualities that differentiate them and recounting examples of the hardships he himself faced in previous lives. Maitreya, on the other hand, has followed the easy bodhisattva vehicle using its skillful means, such as the seven branch practice and the training in the six perfections; the aspirations he thus made are set out in the famous “Prayer of Maitreya” for which this sūtra is perhaps best known. The Buddha declares that Maitreya will become enlightened when sentient beings have fewer negative emotions, in contrast to the ignorant and turbulent beings he himself vowed to help.
                                </p>
                            </section>

