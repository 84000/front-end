                            <h3>Adarsha</h3>
                        </div>
                        <div class="col-sm-9">
                            <p>ADARSHA is an app named by H.H. 17th Karmapa Ogyen Trinley Dorje, that lets users read and conduct searches—in Unicode Tibetan or Wylie—of ancient Buddhist texts in a digital format, namely: the Kangyur (the words of the Buddha translated into Tibetan); the Tengyur (commentaries by Indian scholars translated into Tibetan); and Tibetan Buddhist scriptures.</p>
                            <p>Through this collaboration, 84000 has provided Adarsha with English translations of the titles of all texts in the Kangyur. This is a great step towards providing continuity across English-language referencing of the words of the Buddha.</p>
                            <p>For more information on Adarsha, please visit: <a href="https://adarsha.dharma-treasure.org/about">adarsha.dharma-treasure.org</a></p>
