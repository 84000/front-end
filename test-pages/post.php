                    <h1>84000’s Third Resounding Under the Bodhi Tree</h1>

                    <p>On Thursday, November 10th, Dzongsar Khyentse Chokyi Lodro Institute (DKCLI) and 84000 jointly hosted the “Third Resounding Under the Bodhi Tree” at the Mahabodhi Temple in Bodhgaya, India. The event took place on the concluding day of the 10-day Dzongsar Monlam, and about 1,000 monks and 500 lay attendees participated. Led by Dzongsar Khyentse Rinpoche, Chair of 84000, participants read from two of 84000’s newly translated texts:</p>

                    <ul>
                        <li>
                            <a href="#">The Question of Maitreya (Toh 85)</a>
                        </li>
                        <li>
                            <a href="#">The Question of Maitreya on the Eight Qualities (Toh 86)</a>
                        </li>
                    </ul>

