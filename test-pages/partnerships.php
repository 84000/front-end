                            <h3>Buddhist Digital Resource Center</h3>
                        </div>
                        <div class="col-sm-9">
                            <p>
                                BDRC is a non-profit dedicated to seeking out, preserving, organizing, and disseminating Buddhist literature. Joining digital technology with scholarship, BDRC ensures that the ancient wisdom and cultural treasures of the Buddhist literary tradition are not lost, but are made available for future generations. It was founded as the Tibetan Buddhist Resource Center in 1999 by E. Gene Smith (1936-2010), and since then, BDRC has located, digitized, cataloged, and archived over 15 million pages of culturally significant Buddhist works. BDRC stores the works in its secure archive, built to ensure that the texts remain free and accessible to the public into the indefinite future. Today, BDRC's library is a crucial resource for Tibetan studies, relied on by an international community of academic scholars, religious leaders, translators, Tibetan scholars, publishers, and the interested public.
In 2016, the Tibetan Buddhist Resource Center expanded its mission and became the Buddhist Digital Resource Center. In addition to continuing the work started by E. Gene Smith, BDRC is now tasked with preserving and disseminating all Buddhist literary traditions that remain endangered due to precarious social and environmental factors.
