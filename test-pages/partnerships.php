                            <h3>Buddhist Digital Resource Center</h3>
                        </div>
                        <div class="col-sm-9">
                            <p>BDRC is a non-profit dedicated to seeking out, preserving, organizing, and disseminating Buddhist literature. Joining digital technology with scholarship, BDRC was founded as the Tibetan Buddhist Resource Center in 1999 by E. Gene Smith (1936-2010), and since then, the organization has located, digitized, cataloged, and archived over 15 million pages of culturally significant Buddhist works, storing them in its secure archive ensuring that the texts remain free and accessible to the public into the indefinite future.</p>
                            <p>In 2016, the Tibetan Buddhist Resource Center expanded its mission and became the Buddhist Digital Resource Center. Today, BDRC’s library is a crucial resource for Tibetan studies, relied on by an international community of academic scholars, religious leaders, translators, Tibetan scholars, publishers, and the interested public.</p>
